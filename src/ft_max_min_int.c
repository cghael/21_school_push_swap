#include "push_swap.h"

int		ft_max_min_int(char *str)
{
	int	i;

	i = 0;
	while (str[i] != ' ' && str[i] != '\0')
		i++;
	if (i < 10)
		return (0);
	else if (i > 11 || (i > 10 && (str[0] != '-' && str[0] != '+')))
		return (-1);
	if (str[0] == '-')
		return (ft_strncmp("2147483648", &str[1], 10));
	if (str[0] == '+')
		return (ft_strncmp("2147483647", &str[1], 10));
	return (ft_strncmp("2147483647", &str[0], 10));
}
